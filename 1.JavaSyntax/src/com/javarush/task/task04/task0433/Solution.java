package com.javarush.task.task04.task0433;

/* 
Гадание на долларовый счет
*/

public class Solution {
    public static void main(String[] args) {
        int i=0;
        while (i<10) {
            int j=0;
            while (j<10){
                j++;
                System.out.print("S");
            }
            i++;
            System.out.println();
        }

    }
}
